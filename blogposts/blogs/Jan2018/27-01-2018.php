            <p> IT HAPPENED. I DID IT. I CUT MY HAIR. And my oh my what a rollercoaster it has been, and still is to be honest. I decided to lob off a respectable 11 inches off my hair and for the life of me I can't get used to it. It's such a big change for me and to be honest I still don't know if I like it haha. I'm not reaallly sure what to do with it, and despite so much being taken off, I now feel like I have more hair???? It also actually takes LONGER to style and get ready in the morning. I did not sign up for this. BUT ALAS, life goes on. Anyway he's a little insight into the day I got it cut! Here's my hair before: </p>

            <img class="blogImages" src="../../../images/blogs/Jan2018/27thJan/2.jpg">
            </br>
            <p>
            So I started my day by driving an hour to get back home where my hairdressers is ( I say MY hairdressers, I've only been twice but despite the hefty price tag I know they'd do a good job with this.). I dropped my boyfriend off for a day with my dad and then got to my appointment for half 11. NOW HERE is where I got mad nervous. The first cut. Although I was also excited. One great thing that's come from all this is that my hair was long enough be able to donate to the Little Princess Trust. For those who don't know they use the hair to make wigs for little girls who've lost their hair through illnesses such as cancer. So they put my hair in a ponytail and <i> snip snip snip<i> just like that it was gone.   </p>

            <img class="blogImages" src="../../../images/blogs/Jan2018/27thJan/3.jpg">
            </br></br>
            <p>
            I did the standard thing of running my fingers through my hair, marvelling at how my hair just ends so much earlier than I'm used to, but also breathing a sigh of relief that I didn't look hideous. After this it was pretty relaxing, getting it bleached, washed, then dried, then toned, then washed, then dried again. All in all quite a nice little pamper session, all the while gossiping about love island, dancing on ice, boys, work etc. Lovely jubbly. Then came the final cut. In a moment of self confidence I asked for it shorter than I orignally intended because yano .. go big or go home. So shorter and shorter it got and I was liking the look. I got it straightened too and VOILA I was done. Off I went to live my new, shorter hair, colder necked life.  </p>

            <img class="blogImages" src="../../../images/blogs/Jan2018/27thJan/1.jpg">
            </br></br>
            <p>
            Don't get me wrong I do really like it. Do I like it as much as my old hair? No. Not yet. I'm finding it difficult to deal with. Like when I tried straightening it this week, I had such a battle doing the back because usually I grab my hair and bring it round to the front and do it but now I can't even bring it round so its even in eyesight. And do not even get me started on trying to curl it. I'm sat here with it curled right now, and from the front? It's pretty! The back? God knows how that looks. I couldn't reach nor curl it properly and didn't even want to see how that ended up haha. I'm not so upset by it anymore (bless my boyfriend, I had a good few wobblys over the weekend which he had to deal with. I tried making it wavy and it failed and I cried ahahha, so shout out to him for handling all that like a true gent), but I am pretty burnt up from the curler so. </p>

