            <p> 
            So, like everyone else I've had serious January blues. Christmas is over, it's cold, the wait until payday felt like a lifetime, etc etc. But one thing I can always count on looking forward to is my monthly book box subscription FairyLoot. I've subscribed to this for a whole year now and I'm still obsessed. Some boxes have been pretty weak but regardless I still always want more, because it's far too exciting receiving a box each month filled with surprise goodies!  </p>

            <img class="blogImages" src="../../../images/blogs/Jan2018/31stJan/1.jpg">
            </br>
            <p>
            The theme of this box was 'Talk Faerie to Me' which instantly peaked my interest. I'm a big lover of fantasy and am obsessed with anything written by Sarah J Maas, so knew this box would go down well. Included in this box was some really nice bits and pieces! To start with I pulled out a candle by Meraki Candles called 'Prince Cardan'. I have no idea who that is, apparently it has something to do with the book included in the box so we shall see, but he smells of clove, oak, moss, leather, black pepper and pine which I have to admit does smell good. A wicks and match one would have smelt better though right? ;)))))</p><p> Also included were a couple of magnetic bookmarks of Feyre and Rhysand, which are freaking adorable and both are characters I love, will have to use these for the new book out this May! Also along the Sarah J Maas character line we have some artwork of Rowan and Aelin from the Throne of Glass series, and what you can't see are some quotes on the back of these by other authors who feature fae characters amongst their books. </p>

            <img class="blogImages" src="../../../images/blogs/Jan2018/31stJan/2.jpg">
            </br></br>
            <p>
            Also included was a lip balm by Geeky Clean of which you could get two variations Seelie or Unseelie, I received the Unseelie one which represents the dark side, perfect! It tastes of honey also which is pretty yummy. One of my favourite items in this box was the Faerieland Colouring book. I LOVE colouring and this book is stunning (not to mention made only exclusively for this box ooooo). It features tonnes of beautifully detailed fantasy creatures to colour, the perfect pasttime for this horrible cold weather. Lastly item wise I got a new pillowcase which says "Only you can decide what breaks you" which is a quote from A Court of Wings and Ruin by Sarah J Maas. It's already sitting in my little reading chair. </p>

            <img class="blogImages" src="../../../images/blogs/Jan2018/31stJan/3.jpg">
            </br></br>
            <p>
            Finally, the book of the box, The Cruel Prince by Holly Black. As always the book is a hardback, complete with signed bookplate and a letter from the author! Also the cover is absolutely stunning as I've found all books from fairyloot to be. It's the first book of an upcoming trilogy and here's the blurb. </p>
            <p> <i>"Of course I want to be like them. They're beautiful as blades forged in some divine fire. They will live forever.</br>

            And Cardan is even more beautiful than the rest. I hate him more than all the others. I hate him so much that sometimes when I look at him, I can hardly breathe.</i></br>

            One terrible morning, Jude and her sisters see their parents murdered in front of them. The terrifying assassin abducts all three girls to the world of Faerie, where Jude is installed in the royal court but mocked and tormented by the Faerie royalty for being mortal. </br>

            As Jude grows older, she realises that she will need to take part in the dangerous deceptions of the fey to ever truly belong.</br>

            But the stairway to power is fraught with shadows and betrayal. And looming over all is the infuriating, arrogant and charismatic Prince Cardan . . . "</p>

